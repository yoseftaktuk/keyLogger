openBtn.onclick = () => {
    popup.style.display = "flex";
    scrollingText.style.top = "100%";
    scrollingText.innerText = text;

    let pos = 100; // start below the container
    const interval = setInterval(() => {
        pos -= 1.2; // speed of scrolling
        scrollingText.style.top = pos + "%";
        if (pos + scrollingText.offsetHeight * 100 / 500 < 0) { 
            clearInterval(interval);
            popup.style.display = "none"; 
        }
    }, 20);
};

closeBtn.onclick = () => {
    popup.style.display = "none";
};
